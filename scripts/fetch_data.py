#!/usr/bin/env python3
"""每日复盘 — 腾讯API + AI — GitHub Actions"""
import json, os, sys, re, urllib.request
from datetime import datetime, timezone, timedelta

TZ = timezone(timedelta(hours=8))
NOW = datetime.now(TZ)
TODAY = NOW.strftime('%Y-%m-%d')
W = ['周一','周二','周三','周四','周五','周六','周日'][NOW.weekday()]
if NOW.weekday() >= 5: print("非交易日"); sys.exit(0)

H = {'User-Agent': 'Mozilla/5.0'}
def tx(code):
    req = urllib.request.Request(f'https://qt.gtimg.cn/q={code}', headers=H)
    with urllib.request.urlopen(req, timeout=10) as r:
        return r.read().decode('gbk', errors='ignore')

# ===== 指数 =====
idx_cfg = [('sh000001','上证'),('sz399001','深成指'),('sz399006','创业板'),('sh000688','科创50'),('bj899050','北证50')]
indices = {}
total_vol = 0
for code, name in idx_cfg:
    try:
        t = tx(code)
        parts = t.split('~')
        price = float(parts[3]) if len(parts) > 3 else 0
        m = re.search(r'(\d{14})~(-?[\d.]+)~(-?[\d.]+)~', t)
        chg = float(m.group(3)) if m else 0
        vol = float(parts[6]) / 1e8 if len(parts) > 6 and parts[6].isdigit() else 0
        indices[name] = {'close': round(price,2), 'chg': f'{chg:+.2f}%', 'vol': round(vol)}
        if name in ['上证','深成指']: total_vol += vol
    except Exception as e:
        indices[name] = {'close': 0, 'chg': 'N/A', 'vol': 0}
        print(f"  {name} 失败: {e}")

# ===== 纳指 =====
nas = "N/A"
try:
    t = tx('usIXIC')
    m = re.search(r'(\d{4}-\d{2}-\d{2}[\s\d:]+)~(-?[\d.]+)~(-?[\d.]+)~', t)
    if m: nas = f"{float(m.group(3)):+.2f}%"
except: pass

# ===== 组装 =====
recap = {
    "date":TODAY,"weekday":W,"autoGenerated":True,
    "external":{"nasdaq":nas,"note":"手动补充韩国/费半"},
    "indices":indices,
    "totalVolume":f"{total_vol/10000:.2f}万亿",
    "upCount":0,"downCount":0,"limitUp":0,"limitDown":0,
    "poolStatus":[],"sentiment":{"note":""},
    "cycle":{"stage":"","nature":"","pivot":"6.15","days":(NOW-datetime(2026,6,15,tzinfo=TZ)).days},
    "selfQA":"","strategy":"","visibleLines":"","hiddenLines":"","capitalFlow":"","risks":[""],
    "ladder":[],"keyMoments":[],"auction":[],"newsEvents":[],"divergence":[],"seeSaw":[],"volumeProfile":[]
}

# ===== AI =====
KEY = os.environ.get('DEEPSEEK_KEY','')
if KEY and indices.get('上证',{}).get('close',0) > 0:
    s = f"上证{indices['上证']['close']}({indices['上证']['chg']}) 深{indices['深成指']['chg']} 创{indices['创业板']['chg']} 科创{indices['科创50']['chg']} 成交{recap['totalVolume']} 纳指{nas}"
    prompt = f"A股复盘。{s}根据指数推断今日特征。输出JSON:{{\"sentimentNote\":\"情绪\",\"cycleNature\":\"周期\",\"visibleLines\":\"明线\",\"hiddenLines\":\"暗线\",\"capitalFlow\":\"资金\",\"strategy\":\"策略\",\"risks\":[\"风险1\"]}}"
    try:
        req = urllib.request.Request('https://api.deepseek.com/v1/chat/completions',
            data=json.dumps({"model":"deepseek-chat","messages":[{"role":"user","content":prompt}],"max_tokens":800}).encode(),
            headers={'Content-Type':'application/json','Authorization':f'Bearer {KEY}'})
        with urllib.request.urlopen(req, timeout=30) as r:
            t = json.loads(r.read())['choices'][0]['message']['content']
        m = re.search(r'\{[\s\S]*\}', t)
        if m:
            ai = json.loads(m.group())
            recap['sentiment']['note']=ai.get('sentimentNote','')
            recap['cycle']['nature']=ai.get('cycleNature','')
            recap['visibleLines']=ai.get('visibleLines','')
            recap['hiddenLines']=ai.get('hiddenLines','')
            recap['capitalFlow']=ai.get('capitalFlow','')
            recap['strategy']=ai.get('strategy','')
            recap['risks']=ai.get('risks',[''])
            print("✅ AI已生成")
    except Exception as e: print(f"⚠️ AI: {e}")

data_dir = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))),'data')
os.makedirs(data_dir,exist_ok=True)
path = os.path.join(data_dir,f'{TODAY}.json')
with open(path,'w',encoding='utf-8') as f: json.dump(recap,f,ensure_ascii=False,indent=2)
print(f"✅ {path} | 上证{indices['上证']['close']}({indices['上证']['chg']}) | 纳指{nas}")
